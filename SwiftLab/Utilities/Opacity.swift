import Foundation

extension Double {
    enum Opacity {
        static let transparent = 0.0
        static let veryTranslucent = 0.15
        static let translucent = 0.3
        static let almostTranslucent = 0.5
        static let semiOpaque = 0.6
        static let nearlyOpaque = 0.85
        static let opaque = 1.0
    }
}
