import Foundation

// swiftlint:disable no_cgfloat
extension CGFloat {
    enum Size {
        static let tiny: CGFloat = 24
        static let small: CGFloat = 48
        static let regular: CGFloat = 96
        static let large: CGFloat = 150
        static let huge: CGFloat = 200
        static let massive: CGFloat = 300
    }
}
// swiftlint:enable no_cgfloat
